    private void preencherTabelas() throws SQLException{
          FuncionarioDAO funcionario = new FuncionarioDAO();
          List<Funcionario> funcionarios = funcionario.allFuncionario();
          
          DefaultTableModel tabelaFuncionario = (DefaultTableModel) tblFuncionario.getModel();
          tabelaFuncionario.setNumRows(0);
        
        for(Funcionario f : funcionarios){
            Object[] obj;
            obj = new Object[]{
                f.getIdFuncionario(),
                f.getNomeFuncionario(),
                f.getEmailFuncionario(),
                f.getTelefoneFuncionario(),
                f.getCpfFuncionario(),
                };
            tabelaFuncionario.addRow(obj);
    }
    }

