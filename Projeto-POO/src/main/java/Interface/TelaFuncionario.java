    private void alterarFuncionarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_alterarFuncionarioActionPerformed
        txtEventosV.setText("");
        txtEventosF.setText("");
        if(tblFuncionario.getSelectedRow() != -1){
            try {
                if(nomeFuncionario.getText().isEmpty() || emailFuncionario.getText().isEmpty() || telefoneFuncionario.getText().isEmpty() || cpfFuncionario.getText().isEmpty()){
                    txtEventosF.setText("Confira se não há informações faltando");
                }else{
                FuncionarioDAO funcionario = new FuncionarioDAO();
                Funcionario func = new Funcionario();
                
                func.setIdFuncionario((int)tblFuncionario.getValueAt(tblFuncionario.getSelectedRow(), 0));
                func.setNomeFuncionario(nomeFuncionario.getText());
                func.setEmailFuncionario(emailFuncionario.getText());
                func.setTelefoneFuncionario(telefoneFuncionario.getText());
                func.setCpfFuncionario(cpfFuncionario.getText());
                
                
                funcionario.editarFuncionario(func);
                             
                    nomeFuncionario.setText("");
                    emailFuncionario.setText("");
                    telefoneFuncionario.setText("");
                    cpfFuncionario.setText("");
                    txtEventosV.setText("Atualização de funcionário concluída");
                    
                }
                preencherTabelas();
            } catch (SQLException ex) {
                Logger.getLogger(TelaFuncionario.class.getName()).log(Level.SEVERE, null, ex);
            }
        } 
                                                 

    }//GEN-LAST:event_alterarFuncionarioActionPerformed

