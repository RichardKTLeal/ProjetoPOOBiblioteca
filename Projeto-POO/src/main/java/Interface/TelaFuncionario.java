    private void adicionarFuncionarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_adicionarFuncionarioActionPerformed
        idFuncionario.setText("");
        try {
            
            txtEventosV.setText("");
            txtEventosF.setText("");
            FuncionarioDAO funcionario = new FuncionarioDAO();
            Funcionario func = new Funcionario();
            
            func.setNomeFuncionario(nomeFuncionario.getText());
            func.setEmailFuncionario(emailFuncionario.getText());
            func.setTelefoneFuncionario(telefoneFuncionario.getText());
            func.setCpfFuncionario(cpfFuncionario.getText());
            
            
            if(nomeFuncionario.getText().isEmpty() || emailFuncionario.getText().isEmpty() || telefoneFuncionario.getText().isEmpty() || cpfFuncionario.getText().isEmpty()){
                txtEventosF.setText("Confira se não há informações faltando");
            }else{
                
                DefaultTableModel tabelaFuncionario = (DefaultTableModel) tblFuncionario.getModel();
                
                Object[] dados = {
                    func.getNomeFuncionario(),
                    func.getEmailFuncionario(),
                    func.getTelefoneFuncionario(),
                    func.getCpfFuncionario()
                };
                tabelaFuncionario.addRow(dados);
                
                try {
                    funcionario.inserirFuncionario(func);
                    preencherTabelas();
                    nomeFuncionario.setText("");
                    emailFuncionario.setText("");
                    telefoneFuncionario.setText("");
                    cpfFuncionario.setText("");
                    
                    txtEventosV.setText("Funcionário cadastrado com sucesso");
                } catch (SQLException ex) {
                    Logger.getLogger(TelaFuncionario.class.getName()).log(Level.SEVERE, null, ex);
                    txtEventosF.setText("Ouve um erro ao cadastrar o funcionário");
                    
                }
            }
        } catch (SQLException ex) {
            Logger.getLogger(TelaFuncionario.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_adicionarFuncionarioActionPerformed

