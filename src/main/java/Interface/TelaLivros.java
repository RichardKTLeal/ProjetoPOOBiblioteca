    private void adicionarLivroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_adicionarLivroActionPerformed
        idLivros.setText("");
        try {
            
            txtEventosV.setText("");
            txtEventosF.setText("");
            LivrosDAO livros = new LivrosDAO();
            Livros liv = new Livros();
            
            
            liv.setNomeLivro(nomeLivro.getText());
            liv.setQuantLivro(quantLivro.getText());
            liv.setAutorLivro(autorLivro.getText());
            liv.setEditoraLivro(editoraLivro.getText());
            liv.setPrecoLivro(precoLivro.getText());
            liv.setCategoriaLivro(categoriaLivro.getText());
            
            
            if(nomeLivro.getText().isEmpty() || quantLivro.getText().isEmpty() || autorLivro.getText().isEmpty() || editoraLivro.getText().isEmpty() || precoLivro.getText().isEmpty() || categoriaLivro.getText().isEmpty()){
                txtEventosF.setText("Confira se não há informações faltando");
            }else{
                
                DefaultTableModel tabelaLivros = (DefaultTableModel) tblLivros.getModel();
                
                Object[] dados = {
                    liv.getNomeLivro(),
                    liv.getQuantLivro(),
                    liv.getAutorLivro(),
                    liv.getEditoraLivro(),
                    liv.getPrecoLivro(),
                    liv.getCategoriaLivro()
                };
                tabelaLivros.addRow(dados);
                
                try {
                    livros.inserirLivros(liv);
                    preencherTabelas();
                    nomeLivro.setText("");
                    quantLivro.setText(null);
                    autorLivro.setText("");
                    editoraLivro.setText("");
                    precoLivro.setText("");
                    categoriaLivro.setText("");
                    
                    txtEventosV.setText("Livro cadastrado com sucesso");
                } catch (SQLException ex) {
                    Logger.getLogger(TelaFuncionario.class.getName()).log(Level.SEVERE, null, ex);
                    txtEventosF.setText("Ouve um erro ao cadastrar o livro");
                    
                }
            }
        } catch (SQLException ex) {
            Logger.getLogger(TelaFuncionario.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_adicionarLivroActionPerformed

