    private void alterarLivroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_alterarLivroActionPerformed
       txtEventosV.setText("");
        txtEventosF.setText("");
        if(tblLivros.getSelectedRow() != -1){
            try {
                if(nomeLivro.getText().isEmpty() || quantLivro.getText().isEmpty() || autorLivro.getText().isEmpty() || editoraLivro.getText().isEmpty() || precoLivro.getText().isEmpty() || categoriaLivro.getText().isEmpty()){
                txtEventosF.setText("Confira se não há informações faltando");
            }else{
                LivrosDAO livros = new LivrosDAO();
                Livros liv = new Livros();
                
                
                liv.setNomeLivro(nomeLivro.getText());
                liv.setQuantLivro(quantLivro.getText());
                liv.setAutorLivro(autorLivro.getText());
                liv.setEditoraLivro(editoraLivro.getText());
                liv.setPrecoLivro(precoLivro.getText());
                liv.setCategoriaLivro(categoriaLivro.getText());
                liv.setIdLivros((int)tblLivros.getValueAt(tblLivros.getSelectedRow(), 0));
                
                
                livros.editarLivros(liv);
                             
            idLivros.setText("");
            nomeLivro.setText("");
            quantLivro.setText("");
            autorLivro.setText("");
            editoraLivro.setText("");
            precoLivro.setText("");
            categoriaLivro.setText("");
            txtEventosV.setText("Atualização concluída com sucesso");
                    
                }
                preencherTabelas();
            } catch (SQLException ex) {
                Logger.getLogger(TelaFuncionario.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_alterarLivroActionPerformed

