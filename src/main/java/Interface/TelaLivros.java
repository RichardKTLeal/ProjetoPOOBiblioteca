    private void preencherTabelas() throws SQLException{
          LivrosDAO livro = new LivrosDAO();
          List<Livros> livros = livro.allLivros();
          
          DefaultTableModel tabelaLivros = (DefaultTableModel) tblLivros.getModel();
          tabelaLivros.setNumRows(0);
        
        for(Livros l : livros){
            Object[] obj;
            obj = new Object[]{
                l.getIdLivros(),
                l.getNomeLivro(),
                l.getQuantLivro(),
                l.getAutorLivro(),
                l.getEditoraLivro(),
                l.getPrecoLivro(),
                l.getCategoriaLivro()
                };
            tabelaLivros.addRow(obj);
    }  
    }
